int			ft_checks(int t2, int t3, char *b)
{
	while (b[++t2])
	{
		if (b[t2] != '#' && b[t2] != '.' &&
				b[t2] != '\n' && b[t2] != '\0')
			return (1);
		if (b[t2] == '\n')
		{
			t3++;
			if (t3 % 4 != 0 && b[t2 + 1] == '\n')
				return (1);
		}
		if (t3 % 4 == 0 && t3 != 0 && b[t2 + 1] != '\n' && b[t2 + 1])
			return (1);
		if (t3 % 4 == 0 && t3 != 0 && b[t2 + 1])
		{
			t2++;
			t3 = 0;
		}
	}
	return (0);
}
